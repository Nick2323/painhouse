                            return this.each(function() {

                                var div = $(this),
                                    ul = div.find('ul'),
                                    li = ul.find('li'),
                                    ulPadding = 15,
                                    divWidth = div.width(),
                                    lastLi = ul.find('li:last-child'),
                                    ulWidth = lastLi[0].offsetLeft + lastLi.outerWidth() + ulPadding,
